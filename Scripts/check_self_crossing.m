function intersect = segments_intersect(p1, p2, q1, q2)
    % Helper function to check if two line segments (p1-p2 and q1-q2) intersect
    % Input:
    % p1, p2, q1, q2 - 1x2 vectors representing the endpoints of the segments
    % Output:
    % intersect - a boolean indicating whether the segments intersect

    % Vector cross product
    cross = @(a, b) a(1) * b(2) - a(2) * b(1);

    % Check if points are collinear and on the segment
    on_segment = @(p, q, r) min(p(1), r(1)) <= q(1) && q(1) <= max(p(1), r(1)) && ...
                            min(p(2), r(2)) <= q(2) && q(2) <= max(p(2), r(2));

    % Directions
    d1 = cross(q2 - q1, p1 - q1);
    d2 = cross(q2 - q1, p2 - q1);
    d3 = cross(p2 - p1, q1 - p1);
    d4 = cross(p2 - p1, q2 - p1);

    % Check general case
    if d1 * d2 < 0 && d3 * d4 < 0
        intersect = true;
    % Check special cases
    elseif d1 == 0 && on_segment(q1, p1, q2)
        intersect = true;
    elseif d2 == 0 && on_segment(q1, p2, q2)
        intersect = true;
    elseif d3 == 0 && on_segment(p1, q1, p2)
        intersect = true;
    elseif d4 == 0 && on_segment(p1, q2, p2)
        intersect = true;
    else
        intersect = false;
    end
end
